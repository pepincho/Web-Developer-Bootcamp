// Recap

// 1. the keyword 'this' is a reserved keyword in JavaScript and
// its value is determined at execution

// 2. it's either set using the global context, object binding,
// explicit binding, or the new keyword

// 3. when set in the global context in a function, it is either the
// global object (window if in the browser) or indefined (if we
// are using strict mode)

// 4. to explicitly set the value of the keyword 'this', we use call,
// apply or bind

// 5. we can also use the 'new' keyword to set the context of
// 'this', which we will discuss when we talk about OOP
